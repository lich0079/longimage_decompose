package longimage_decompose;

public class Rect {

    int top = -1;
    int bottom;
    int left = -1;
    int right;
    
    public Rect() {
        super();
    }

    @Override
    public String toString() {
        return "Rect [top=" + top + ", bottom=" + bottom + ", left=" + left + ", right=" + right + "]";
    }
}
